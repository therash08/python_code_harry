# Arithmetic Operators

a = 10
b = 2
print("Addition:", a + b)          # Output: 6
print("Subtraction:", a - b)       # Output: 2
print("Multiplication:", a * b)    # Output: 15
print("Division:", a / b)          # Output: 2.5
print("Floor Division:", a // b)   # Output: 2
print("Modulus:", a % b)           # Output: 1
print("Exponentiation:", a ** b)   # Output: 25


# assignment Operators

x = 5
x += 3  # Equivalent to x = x + 3
print("After += :", x)  # Output: 8
x -= 2  # Equivalent to x = x - 2
print("After -= :", x)  # Output: 6
x *= 4  # Equivalent to x = x * 4
print("After *= :", x)  # Output: 24
x /= 3  # Equivalent to x = x / 3
print("After /= :", x)  # Output: 8.0


# Comparison Operators

p = 10
q = 20
print("Equal:", p == q)            # Output: False
print("Not Equal:", p != q)        # Output: True
print("Greater Than:", p > q)      # Output: False
print("Less Than:", p < q)         # Output: True
print("Greater Than or Equal To:", p >= q)  # Output: False
print("Less Than or Equal To:", p <= q)     # Output: True


