a = 10  # a is an integer

b = 5.5  # b is a float

c = "Hello, World!"  # c is a string
d = True  # d is a boolean
e = [1, 2, 3, 4, 5]  # e is a list
f = (1, 2, 3)  # f is a tuple
g = {"name": "Alice", "age": 25}  # g is a dictionary
h = {1, 2, 3, 4}  # h is a set


# Print the data types
print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>
print(type(c))  # <class 'str'>
print(type(d))  # <class 'bool'>
print(type(e))  # <class 'list'>
print(type(f))  # <class 'tuple'>
print(type(g))  # <class 'dict'>
print(type(h))  # <class 'set'>
# Demonstrating type conversion
x = 5  # integer
y = 2.5  # float
print(float(x))  # Convert integer to float
print(int(y))  # Convert float to integer
print(str(x))  # Convert integer to string
print(int("10"))  # Convert string to integer
print(float("3.14"))  # Convert string to float
print(list((1, 2, 3)))  # Convert tuple to list
print(tuple([1, 2, 3]))  # Convert list to tuple
print(set([1, 2, 2, 3]))  # Convert list to set

# Demonstrating variable assignment
var1 = 100
var2 = var1 + 50
print(var2)  # Output: 150
